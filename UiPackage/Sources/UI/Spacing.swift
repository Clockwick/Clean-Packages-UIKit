import Foundation
/// Enumeration representing various spacing values.
/// - none: No spacing (0 points).
/// - xs2: Extra-extra-small spacing (2 points).
/// - xs: Extra-small spacing (4 points).
/// - sm: Small spacing (8 points).
/// - md: Medium spacing (12 points).
/// - lg: Large spacing (16 points).
/// - xl: Extra-large spacing (20 points).
/// - xl2: Double extra-large spacing (24 points).
/// - xl3: Triple extra-large spacing (32 points).
/// - xl4: Quadruple extra-large spacing (40 points).
/// - xl5: Quintuple extra-large spacing (48 points).
/// - xl6: Sextuple extra-large spacing (56 points).
public enum Spacing {
    /// No spacing (0 points).
    public static let none: CGFloat = 0
    
    /// Extra-extra-small spacing (2 points).
    public static let xs2: CGFloat = 2
    
    /// Extra-small spacing (4 points).
    public static let xs: CGFloat = 4
    
    /// Small spacing (8 points).
    public static let sm: CGFloat = 8
    
    /// Medium spacing (12 points).
    public static let md: CGFloat = 12
    
    /// Large spacing (16 points).
    public static let lg: CGFloat = 16
    
    /// Extra-large spacing (20 points).
    public static let xl: CGFloat = 20
    
    /// Double extra-large spacing (24 points).
    public static let xl2: CGFloat = 24
    
    /// Triple extra-large spacing (32 points).
    public static let xl3: CGFloat = 32
    
    /// Quadruple extra-large spacing (40 points).
    public static let xl4: CGFloat = 40
    
    /// Quintuple extra-large spacing (48 points).
    public static let xl5: CGFloat = 48
    
    /// Sextuple extra-large spacing (56 points).
    public static let xl6: CGFloat = 56
}
